export const MapComponent: React.FC<MapProps> = ({
  center,
  crowdDensityData,
}) => {
  const heatmapData = crowdDensityData.map(item => {
    const [lat, lng] = item.location.split(',').map(parseFloat);
    return {
      location: new google.maps.LatLng(lat, lng),
      weight: item.density,
    };
  });

  return (
    <GoogleMap
      mapContainerStyle={containerStyle}
      center={center}
      zoom={14}
      options={mapOptions}
    >
      <MarkerF position={center} />
      {heatmapData.length > 0 && (
        <HeatmapLayerF
          data={heatmapData}
          options={{
            radius: 30,
            opacity: 0.8,
          }}
        />
      )}
    </GoogleMap>
  );
};
